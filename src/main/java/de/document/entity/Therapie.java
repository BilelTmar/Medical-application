package de.document.entity;

/**
 *
 * @author Bilel-PC
 */
public class Therapie {
    private String notfall ;
    private String initialtherapie ;
    private String monitoring ;
    private String weiteresTherapieregime ;

    public String getNotfall() {
        return notfall;
    }

    public void setNotfall(String notfall) {
        this.notfall = notfall;
    }

    public String getInitialtherapie() {
        return initialtherapie;
    }

    public void setInitialtherapie(String initialtherapie) {
        this.initialtherapie = initialtherapie;
    }

    public String getMonitoring() {
        return monitoring;
    }

    public void setMonitoring(String monitoring) {
        this.monitoring = monitoring;
    }

    public String getWeiteresTherapieregime() {
        return weiteresTherapieregime;
    }

    public void setWeiteresTherapieregime(String weiteresTherapieregime) {
        this.weiteresTherapieregime = weiteresTherapieregime;
    }
    
    
}

package de.document.entity;

/**
 *
 * @author Bilel-PC
 */
public class Diagnostik {

    private String notfall ;
    private String leitsymptome ;
    private String anamnese ;
    private String klinischeUntersuchung ;
    private String apperativeDiagnostik ;
    private String differentialdiagnosen ;
    private String erweiterteDiagnostik ;

    public String getNotfall() {
        return notfall;
    }

    public void setNotfall(String notfall) {
        this.notfall = notfall;
    }

    public String getLeitsymptome() {
        return leitsymptome;
    }

    public void setLeitsymptome(String leitsymptome) {
        this.leitsymptome = leitsymptome;
    }

    public String getAnamnese() {
        return anamnese;
    }

    public void setAnamnese(String anamnese) {
        this.anamnese = anamnese;
    }

    public String getKlinischeUntersuchung() {
        return klinischeUntersuchung;
    }

    public void setKlinischeUntersuchung(String klinischeUntersuchung) {
        this.klinischeUntersuchung = klinischeUntersuchung;
    }

    public String getApperativeDiagnostik() {
        return apperativeDiagnostik;
    }

    public void setApperativeDiagnostik(String apperativeDiagnostik) {
        this.apperativeDiagnostik = apperativeDiagnostik;
    }

    public String getDifferentialdiagnosen() {
        return differentialdiagnosen;
    }

    public void setDifferentialdiagnosen(String differentialdiagnosen) {
        this.differentialdiagnosen = differentialdiagnosen;
    }

    public String getErweiterteDiagnostik() {
        return erweiterteDiagnostik;
    }

    public void setErweiterteDiagnostik(String erweiterteDiagnostik) {
        this.erweiterteDiagnostik = erweiterteDiagnostik;
    }
    

}

public class Vorbereitung {
    private String notfall ;
    private String indikation;
    private String kontraindikation;

    public String getNotfall() {
        return notfall;
    }

    public void setNotfall(String notfall) {
        this.notfall = notfall;
    }

    public String getIndikation() {
        return indikation;
    }

    public void setIndikation(String indikation) {
        this.indikation = indikation;
    }

    public String getKontraindikation() {
        return kontraindikation;
    }

    public void setKontraindikation(String kontraindikation) {
        this.kontraindikation = kontraindikation;
    }
    
    
}

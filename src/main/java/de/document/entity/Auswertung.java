package de.document.entity;

/**
 *
 * @author Bilel-PC
 */
public class Auswertung {
    private String notfall ;
    private String auswertung;
    private String interpretation;
    private String weiteresProzedere;

    public String getNotfall() {
        return notfall;
    }

    public void setNotfall(String notfall) {
        this.notfall = notfall;
    }

    public String getAuswertung() {
        return auswertung;
    }

    public void setAuswertung(String auswertung) {
        this.auswertung = auswertung;
    }

    public String getInterpretation() {
        return interpretation;
    }

    public void setInterpretation(String interpretation) {
        this.interpretation = interpretation;
    }

    public String getWeiteresProzedere() {
        return weiteresProzedere;
    }

    public void setWeiteresProzedere(String weiteresProzedere) {
        this.weiteresProzedere = weiteresProzedere;
    }
    
    
}
